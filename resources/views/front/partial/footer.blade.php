   function cityfilter(id){
        var d_id =id  ;

        if(d_id){
            $.ajax({
                type:"GET",
                url:"{{url('get-city-list')}}?d_id="+d_id,
                success:function(res){
                    if(res){
                        $("#city").empty();
                        $.each(res,function(key,value){
                            $("#city").append('<option value="'+key+'">'+value+'</option>');
                        });

                    }else{
                        $("#city").empty();
                    }
                }
            });
        }else{
            $("#city").empty();
        }

    };
</script>

